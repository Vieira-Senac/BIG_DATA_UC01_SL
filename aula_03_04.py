# Programa Igual ou Diferente
n1 = int(input("Informe o primeiro valor: "))
n2 = int(input("Informe o segundo valor: "))
if n1 == n2:
    # produto = n1 * n2
    print(f"Os valores são iguais, portanto o produto deles é {n1 * n2}")
else:
    # soma = n1 + n2
    print(f"Os valores são diferentes, portanto a soma deles é {n1 + n2}.")